package Revisao_Geral;

public class NumerosImparesAte20 {

    public static void main(String[] args) {
        //Loop de 0 a 20
        for (int i = 0; i <= 20; i++) {
            //Verifica se o número é ímpar
            if (i % 2 != 0) {
                //Exibe os números ímpares
                System.out.println(i);
            }
        }
    }
}
